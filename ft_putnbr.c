#include "ft_printf.h"

void	ft_putnbr(int nb)
{
	char	c;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	if (nb == 2147483647)
	{
		write(1, "2147483648", 10);
		return ;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	c = nb % 10 + '0';
	ft_putchar(c);
}